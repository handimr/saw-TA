     * @param  \App\Salary  $salary
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, Salary $salary)
    {
        $request->validate([
                'name' => 'required',
                'bobot_salary' => 'required'
        ]);

        Salary::where('id',$salary->id)
                ->update([
                    'name' =>$request->name,
                    'bobot_salary' =>$request->bobot_salary
                ]);
        return redirect('/salaries')->with('status','Data salary berhasil di ubah'); 
    }


